
public class Math {

	public void addition(int a, int b){
		
		int c;
		c= a + b;
		System.out.println("Addition");
		System.out.println("------------");
		System.out.println(a + " + " + b + " = " + c);
	}
	public void subtraction(int a, int b){
		
		int c;
		c = a - b;
		System.out.println("Subtraction");
		System.out.println("------------");
		System.out.println(a + " - " + b + " = " + c);
	}
	
}
